SCRIPT MACRO PHR,VAL
    MOV DX,OFFSET PHR
    MOV AH,09H
    INT 21H
    
    MOV AH,01H
    INT 21H
    SUB AL,30H
    MOV VAL,AL
    

ENDM



MENUOP MACRO OP,NW
    MOV DX,OFFSET OP
    MOV AH,09H
    INT 21H
    
    MOV DX,OFFSET NW
    MOV AH,09H
    INT 21H
ENDM


PLUS MACRO VAL1,VAL2 
    MOV AL,VAL1
    ADD VAL2,AL
    
    MOV DX,VAL2
    ADD DX,48H
    MOV AL,02H
    INT 21H
ENDM






SCRIPT PHR1,VAL1  
SCRIPT PHR1,VAL2
MENUOP OP1,NW
MENUOP OP2,NW
MENUOP OP3,NW
MENUOP OP4,NW
SCRIPT PHR2,VAL3
CMP VAL3,'1'
JE PLUS VAL1,VAL2





